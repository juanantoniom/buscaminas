package buscaminas.model;

//We create the elements of the board
public class BoardElement {

	private int fila;
	
	private int columna;
	
	
	public int getFila() {
		return fila;
	}
	public void setFila(int fila) {
		this.fila = fila;
	}
	public int getColumna() {
		return columna;
	}
	public void setColumna(int columna) {
		this.columna = columna;
	}
	
	
}
